"""
1. 문제 이해하기
- N행 4열의 표(land)가 있음.
- 한 행에서 한 칸만 선택하면서 내려감.
- 같은 열을 연속해서 밟을 수 없음.
- 마지막 행까지 내려왔을 때, 얻을 수 있는 점수의 최대값을 구하기.

2. 풀이 방향
- 첫 번째 행은 그대로 둔다. (초기값)
- 두 번째 행부터 N번째 행까지, 각 칸(land[i][j])에 이전 행에서 "같은 열이 아닌 최댓값"을 더해서 누적한다.
- 마지막 행에서 최댓값을 찾는다. (결과값)
"""

def solution(land):
    # 2번째 행부터 마지막 행까지 반복
    for i in range(1, len(land)):  
        # 현재 행의 4칸을 순회
        for j in range(len(land[i])):  
            arr = []  # 이전 행에서 같은 열을 제외한 값들을 저장할 리스트
            
            # 이전 행의 모든 열을 순회하면서 같은 열이 아닌 값들을 리스트에 추가
            for k in range(len(land[i])):  
                if j != k:  # 같은 열은 제외
                    arr.append(land[i-1][k])  
                    
            # 현재 칸의 값을 이전 행에서 선택 가능한 최댓값과 합산하여 업데이트
            land[i][j] += max(arr)  
    
    # 마지막 행에서 가장 큰 값을 찾아 반환
    return max(land[-1])  
