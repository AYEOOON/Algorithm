'''
https://school.programmers.co.kr/learn/courses/30/lessons/12938
: 합이 S가 되면서, 곱이 제일 큰 집합을 구하는 문제

[초기 접근]
처음에는 가능한 모든 조합을 만들어서 곱이 최대가 되는 경우를 찾는 방식으로 접근했다.

1부터 9까지 숫자를 이용해 n개의 숫자를 뽑는 중복 조합을 생성하고
각 조합의 합이 s인지 확인한 뒤 곱이 가장 큰 경우를 선택했다.

이 방법은 효율성 테스트에서 실패했다.

이유를 생각해 보니 모든 조합을 생성하는 과정에서 경우의 수가 급격하게 증가한다.
n이 조금만 커져도 수백만 개의 조합이 생성된다. 각 조합마다 합 계산, 곱 계산을 반복해야 하므로 시간 복잡도가 매우 커진다.

즉 이 문제는 완전 탐색으로 풀기에는 비효율적인 문제였다.

[다시 생각해본 접근]
문제를 다시 보면서 이런 생각이 들었다.

같은 합이라면 어떤 숫자 조합이 곱이 가장 클까?
예를 들어 합이 10일 때

1,1,8 → 곱 8
2,3,5 → 곱 30
3,3,4 → 곱 36

숫자들이 서로 비슷할수록 곱이 커진다는 것을 발견했다. 즉 곱을 최대화하려면 숫자들을 최대한 균등하게 나누면 된다.

[풀이 알고리즘]
n개의 숫자가 합 s가 되도록 최대한 균등하게 분배한다.

1. s // n
  → 기본값
2. s % n
  → 나머지
3. 나머지를 뒤에서부터 +1씩 분배

이 문제의 핵심은 완전 탐색이 아니라 수학적 성질을 이용하는 것이었다.

합이 일정할 때 곱을 최대화하려면 값들이 서로 최대한 균등해야 한다.
이 성질을 이용하면 모든 조합을 탐색할 필요 없이 바로 답을 구할 수 있다.
'''

def solution(n, s):
    if s < n:
        return [-1]
    
    base = s//n
    mod = s%n
    answer = [base]*n
    
    for i in range(mod):
        answer[n-1-i] += 1
        
    return answer
